    clf = pipeline("zero-shot-classification", model=ZS_MODEL, device=device)

    ds = Dataset.from_dict({"text": texts})

    def infer(batch):
        out = clf(batch["text"], labels)
        return {"pred": [r["labels"][0] for r in out]}

    ds = ds.map(infer, batched=True, batch_size=32)
    return ds["pred"]

