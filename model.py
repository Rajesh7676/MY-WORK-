# ─────────────────────────────
# CLEAN LLM OUTPUT
# ─────────────────────────────
def clean_output(text: str) -> str:
    bad_patterns = [
        "insert company", "logo", "website",
        "phone number", "address", "[Insert"
    ]
    for b in bad_patterns:
        text = text.replace(b, "")

    # remove repeated blank noise
    text = re.sub(r"\n{3,}", "\n\n", text)

    return text.strip()


